let x = 10;
console.log("initial value :", x);

// using +=
x += 5;
console.log("after  X+= :", x);

// using -=
x -= 3;
console.log("after  X-= :", x);

// using *=
x *= 5;
console.log("after  X*= :", x);

// using /=
x /= 5;
console.log("after  X/= :", x);

// using %=
x %= 5;
console.log("after  X%= :", x);
