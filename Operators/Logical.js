// Logical AND(&&)
console.log('True && True :', true && true);
console.log('True && False :', true && false);

let a = 2;
let b = 21;

let x = a > b && b > a;
console.log("AND :",x);

// Logical OR(||)
console.log('True || False :', true || true);
console.log('False || False :', false || false);

let y = a > b || b > a;
console.log("OR :",y);

// Logical NOT(!)
console.log("!-True :",!true);

let z=!(a>b) // !(2 > 21)
console.log("NOT :",z);




